    public void search()
    {
        int a1=Integer.parseInt(jTextField13.getText());
        
        String sql="Select * from New_Client where Client_ID="+a1;
        try
        {
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next())
            {
                jTextField13.setText(rs.getString(1));
                jTextField14.setText(rs.getString(2));
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Incorrect Client ID");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Please enter correct fill");
        }
    }
    
